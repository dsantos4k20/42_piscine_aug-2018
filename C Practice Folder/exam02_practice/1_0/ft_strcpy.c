#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

char	*ft_strcpy(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}

int		main()
{
	char str1[] = "String 1";
	char str2[] = "String 2";

	ft_putstr(str1);
	ft_putchar('\n');
	ft_strcpy(str1, str2);
	ft_putstr(str1);
	ft_putchar('\n');
}
